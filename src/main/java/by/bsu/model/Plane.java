package by.bsu.model;

public class Plane {

	private int id_plane;
	private String plane_name;
	private int id_ticket_plane;
	private int id_class_plane;
	
	
	

	public int getId_plane() {
		return id_plane;
	}
	
	public void setId_plane(int id_plane) {
		this.id_plane = id_plane;
	}
	public String getPlane_name() {
		return plane_name;
	}
	public void setPlane_name(String plane_name) {
		this.plane_name = plane_name;
	}
	public int getId_ticket_plane() {
		return id_ticket_plane;
	}
	public void setId_ticket_plane(int id_ticket_plane) {
		this.id_ticket_plane = id_ticket_plane;
	}
	public int getId_class_plane() {
		return id_class_plane;
	}
	public void setId_class_plane(int id_class_plane) {
		this.id_class_plane = id_class_plane;
	}
	
}
